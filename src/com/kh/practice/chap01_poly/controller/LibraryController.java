package com.kh.practice.chap01_poly.controller;

import com.kh.practice.chap01_poly.model.vo.AniBook;
import com.kh.practice.chap01_poly.model.vo.Book;
import com.kh.practice.chap01_poly.model.vo.CookBook;
import com.kh.practice.chap01_poly.model.vo.Member;

public class LibraryController {
	private Member mem;
	private Book[] bList;

	public static int SIZE = 5;
	{
		mem = null;	// 왜 null값으로 초기화 하는걸까?
		bList = new Book[SIZE];
		bList[0] = new CookBook("백종원의 집밥", "백종원", "tvN", true);
		bList[1] = new AniBook("한번 더 해요", "미티", "원모어", 19);
		bList[2] = new AniBook("루피의 원피스", "루피", "japan", 12);
		bList[3] = new CookBook("이혜정의 얼마나 맛있게요", "이혜정", "문학", false);
		bList[4] = new CookBook("최현석 날 따라해봐", "최현석", "소금책", true);
	}
	
	public void insertMember(Member mem) {
		this.mem = mem;
	}
	public Member myInfo() {
		return mem;
	}
	public Book[] selectAll() {
		return bList;
	}
	public Book[] seachBook(String keyword) {
		Book[] sBook = new Book[SIZE];
		int count = 0;
		for(int i=0; i<SIZE; i++) {
			if(bList[i].getTitle().contains(keyword)) {
				sBook[count] = bList[i];
			}
			count++;
		}
		return sBook;
		
	}
	public int rentBook(int index) {
		int result = 0;
		if(bList[index] instanceof CookBook) {
			boolean coupon = ((CookBook)bList[index]).isCoupon();
			if(coupon == true) {
				mem.setCouponCount();
				result = 2;
				return result;
			}
		}
		else if(bList[index] instanceof AniBook) {
			int age = ((AniBook)bList[index]).getAccessAge();
			if(age <= 19) {
				result = 1;
				return result;
			}
		}
		return result;
		
	}
	
}
